

/// demo to show how to declare a typescript thing in external file and export it ////////////////
  interface User {
    name: string;
    id: number;
  }
   
  class UserAccount {
    name: string;
    id: number;
   
    constructor(name: string, id: number) {
      this.name = name;
      this.id = id;
    }
  }

export { User, UserAccount }